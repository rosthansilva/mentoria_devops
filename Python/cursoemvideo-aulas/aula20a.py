def soma(a, b):                                 # definição de função com os parametros correspondentes
    print('=-' * 30)                            # linha com 30 espaços
    print(f'A = {a} e B = {b}')                 # print formatado informando os valores de A e B
    s = a + b                                   # soma simples de a + b
    print(f'A soma de A + B = {s}')             # print variavel S contendo a soma de a + b
    print('=-' * 30)                            # linha com 30 espaços
    print()


# programa principal
soma(5, 8)                                      # soma(a,b)
soma(999, 999)
soma(6.8, 9.9)
